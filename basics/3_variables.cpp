#include <iostream>
using namespace std;
int main()
{
    //Variables are used to store data.
    //It is very important to first indicate what type a variable is going to have.
    int tuna = 6; //int can hold integers and tuna is the name.
    cout << tuna << endl << endl; //just prints 6 to the screen
    // we can use endl more than once also.
    int a = 4;
    int b = 21;
    int sum = a + b; /*Aside from just assigning we can perform basic 
    arithmetic on them.*/
    cout << sum;
    return 0;
}